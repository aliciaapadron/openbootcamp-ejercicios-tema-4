//Crea un bucle While, este bucle tendrá que tener como condición que la variable numeroWhile sea inferior a 3
package main;

class Main {
	public static void main(String[] args) {
	   int numeroWhile = 1;

		while (numeroWhile <= 3){
        System.out.println(numeroWhile);
        numeroWhile += 1;
	}
	}
}